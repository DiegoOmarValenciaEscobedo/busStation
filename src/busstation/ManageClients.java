    private void ModifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyButtonActionPerformed
        int i = ClientsTable.getSelectedRow();
        if (i == -1)
            JOptionPane.showMessageDialog(null, "No has seleccionado nada", "Error", JOptionPane.ERROR_MESSAGE);
        else {
            try {
                int id = Integer.parseInt(ClientsTable.getValueAt(i, 0).toString());
                Statement st;
                st = con.createStatement();
                if (String.valueOf(ClientsTable.getValueAt(i, 6)).substring(0, 1).equalsIgnoreCase("C")) {
                    st.executeUpdate("UPDATE client SET Name='" + NameTextField.getText() + "', LastName='"
                            + LastNameTextField.getText() + "', PhoneNumber=" + PhoneNumberTextField.getText() + ", Address='"
                            + AddressTextField.getText() + "', Gender='" + GenderComboBox.getSelectedItem().toString().substring(0, 1) + "', BirthDate='"
                            + BirthDateTextField.getText() + "' WHERE IdClient=" + id);
                } else {
                    st.executeUpdate("UPDATE admin SET Name='" + NameTextField.getText() + "', LastName='"
                            + LastNameTextField.getText() + "', PhoneNumber=" + PhoneNumberTextField.getText() + ", Address='"
                            + AddressTextField.getText() + "', Gender='" + GenderComboBox.getSelectedItem().toString().substring(0, 1) + "', BirthDate='"
                            + BirthDateTextField.getText() + "' WHERE IdAdmin=" + id);
                }

                ReadDataBase();
                JOptionPane.showMessageDialog(null, "Se Actualizaron los datos del cliente", "Listo", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
            }
        }
    }//GEN-LAST:event_ModifyButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        int i = ClientsTable.getSelectedRow();
        if (i == -1)
            JOptionPane.showMessageDialog(null, "No has seleccionado nada.", "Error", JOptionPane.ERROR_MESSAGE);
        else {
            try {
                int id = Integer.parseInt(ClientsTable.getValueAt(i, 0).toString());
                Statement st;
                st = con.createStatement();
                if (String.valueOf(ClientsTable.getValueAt(i, 6)).substring(0, 1).equalsIgnoreCase("c")) {
                    st.executeUpdate("DELETE FROM client WHERE (IdClient='" + id +"');");
                } else if (String.valueOf(ClientsTable.getValueAt(i, 6)).substring(0, 1).equalsIgnoreCase("A")) {
                    st.executeUpdate("DELETE FROM admin WHERE (IdAdmin='" + id +"');");
                }
                DefaultTableModel table = (DefaultTableModel) ClientsTable.getModel();
                table.removeRow(i);
                JOptionPane.showMessageDialog(null, "Se elimino el usuario", "Listo", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
            }
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        try {

            boolean exists = false;
            String comp = "";
            String user = NameTextField.getText() + " " + LastNameTextField.getText();
            user = user.toLowerCase();

            Statement stF = con.createStatement();
            ResultSet rsF = stF.executeQuery("SELECT * FROM admin");

            while (rsF.next()) {
                String A = rsF.getString("Name");
                String B = rsF.getString("LastName");
                comp = A + " " + B;
                comp = comp.toLowerCase();
                if (comp.equalsIgnoreCase(user)) {
                    exists = true;
                }
            }
            stF.close();

            Statement stG = con.createStatement();
            ResultSet rsG = stG.executeQuery("SELECT * FROM client");

            while (rsG.next()) {
                String A = rsG.getString("Name");
                String B = rsG.getString("LastName");
                comp = A + " " + B;
                comp = comp.toLowerCase();
                if (comp.equalsIgnoreCase(user)) {
                    exists = true;
                }
            }
            stG.close();

            Statement st = con.createStatement();
            if ("".equals(NameTextField.getText()) || "".equals(LastNameTextField.getText()) || "".equals(BirthDateTextField.getText())
                    || "".equals(AddressTextField.getText()) || "".equals(PhoneNumberTextField.getText()) || "".equals(String.valueOf(PasswordField1.getPassword()))) {
                JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (exists == true) {
                JOptionPane.showMessageDialog(null, "Nombre ya existente en el sistema!", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (!Arrays.equals(PasswordField1.getPassword(), PasswordField2.getPassword())) {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                if (TypeComboBox.getSelectedItem().toString().equalsIgnoreCase("Cliente")) {
                    st.execute("INSERT INTO `client` (`IdClient`,"
                            + " `Name`, `LastName`, `PhoneNumber`, `Address`, `Gender`, `BirthDate`, `Password`)"
                            + "VALUES (NULL,'"
                            + NameTextField.getText() + "','"
                            + LastNameTextField.getText() + "',"
                            + PhoneNumberTextField.getText() + ",'"
                            + AddressTextField.getText() + "','"
                            + String.valueOf(GenderComboBox.getSelectedItem()).substring(0, 1) + "','"
                            + BirthDateTextField.getText() + "','"
                            + String.valueOf(PasswordField1.getPassword()) + "');");
                } else {
                    st.execute("INSERT INTO `admin` (`IdAdmin`, "
                            + " `Name`, `LastName`, `PhoneNumber`, `Address`, `Gender`, `BirthDate`, `Password`)"
                            + "VALUES (NULL,'"
                            + NameTextField.getText() + "','"
                            + LastNameTextField.getText() + "','"
                            + PhoneNumberTextField.getText() + "','"
                            + AddressTextField.getText() + "','"
                            + GenderComboBox.getSelectedItem().toString().substring(0, 1) + "','"
                            + BirthDateTextField.getText() + "','"
                            + String.valueOf(PasswordField1.getPassword()) + "');");
                }
                JOptionPane.showMessageDialog(null, "El usuario ha sido agregado a la lista", "Listo", JOptionPane.INFORMATION_MESSAGE);
                ReadDataBase();
            }
        } catch (SQLException ex) {
            Logger.getLogger(LogIn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_AddButtonActionPerformed

