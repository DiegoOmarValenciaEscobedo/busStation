    public void ReadDataBase() {
        int i = 0;
        int rn = ClientsTable.getRowCount();
        DefaultTableModel table = (DefaultTableModel) ClientsTable.getModel();
        while (i < rn) {
            table.removeRow(0);
            i++;
        }
        try {
            Statement st;
            st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM client;");
            while (rs.next()) {
                String Row[] = new String[]{rs.getString("IdClient"), rs.getString("Name") + " " + rs.getString("LastName"),
                    rs.getString("PhoneNumber"), rs.getString("Address"), rs.getString("Gender"), rs.getString("BirthDate"), "Cliente"};
                table.addRow(Row);
            }
        } catch (SQLException ex) {
        }
        try {
            Statement st;
            st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM admin;");
            while (rs.next()) {
                String Row[] = new String[]{rs.getString("IdAdmin"), rs.getString("Name") + " " + rs.getString("LastName"),
                    rs.getString("PhoneNumber"), rs.getString("Address"), rs.getString("Gender"), rs.getString("BirthDate"), "Administrador"};
                table.addRow(Row);
            }
        } catch (SQLException ex) {
        }
    }

