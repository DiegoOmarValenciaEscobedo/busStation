    private void ModifyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyButtonActionPerformed
        int i = BusesTable.getSelectedRow();
        if (i == -1)
            JOptionPane.showMessageDialog(null, "No has seleccionado nada", "Error", JOptionPane.ERROR_MESSAGE);
        else {
            try {
                int id = Integer.parseInt(BusesTable.getValueAt(i, 0).toString());
                Statement st;
                st = con.createStatement();
                st.executeUpdate("UPDATE bus SET Model='" + ModelComboBox.getSelectedItem().toString().substring(0, 1) + "', Enrollment='"
                        + EnrollmentTextField.getText() + "', PurchaseDate='" + PurchaseDateTextField.getText() + "', PropertyOf='"
                        + PropertyComboBox.getSelectedItem().toString().substring(0, 1) + "', Driver='"
                        + DriverComboBox.getSelectedItem().toString().substring(0, 1) + "' WHERE IdBus=" + id);
                ReadDataBase();
                JOptionPane.showMessageDialog(null, "Se Actualizaron los datos del autobus", "Listo", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
            }
        }
    }//GEN-LAST:event_ModifyButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        int i = BusesTable.getSelectedRow();
        if (i == -1)
            JOptionPane.showMessageDialog(null, "No has seleccionado nada", "Error", JOptionPane.ERROR_MESSAGE);
        else {
            try {
                int id = Integer.parseInt(BusesTable.getValueAt(i, 0).toString());
                Statement st;
                st = con.createStatement();
                st.executeUpdate("DELETE FROM bus WHERE IdBus=" + id);
                DefaultTableModel table = (DefaultTableModel) BusesTable.getModel();
                table.removeRow(i);
                JOptionPane.showMessageDialog(null, "Se elimino el autobus de la lista", "Listo", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "El dato que quieres eliminar esta ligado a otro registro", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        try {
            boolean exists = false;
            String roll = EnrollmentTextField.getText();
            roll = roll.toLowerCase();

            Statement stE = con.createStatement();
            ResultSet rsE = stE.executeQuery("SELECT * FROM bus");

            while (rsE.next()) {
                String comp = rsE.getString("Enrollment");
                comp = comp.toLowerCase();
                if (comp.equalsIgnoreCase(roll)) {
                    exists = true;
                }
            }
            stE.close();

            if (exists == true) {
                JOptionPane.showMessageDialog(null, "Ya existe una matricula igual en el sistema!", "Error", JOptionPane.ERROR_MESSAGE);
            } else {

                Statement st = con.createStatement();
                if ("".equals(EnrollmentTextField.getText()) || "".equals(PurchaseDateTextField.getText())) {
                    JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    st.execute("INSERT INTO `bus` (`IdBus`, `Model`, `Enrollment`, `PurchaseDate`, `PropertyOf`, `Driver`) "
                            + "VALUES (NULL,'" + ModelComboBox.getSelectedItem().toString().substring(0, 1) + "','"
                            + EnrollmentTextField.getText() + "','" + PurchaseDateTextField.getText() + "','"
                            + PropertyComboBox.getSelectedItem().toString().substring(0, 1) + "','" + DriverComboBox.getSelectedItem().toString().substring(0, 1) + "')");
                    JOptionPane.showMessageDialog(null, "El autobus se añadio a la lista", "Listo", JOptionPane.INFORMATION_MESSAGE);
                    ReadDataBase();
                }
            }
        } catch (SQLException ex) {
        }
    }//GEN-LAST:event_AddButtonActionPerformed

