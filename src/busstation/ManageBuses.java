    public void ReadDataBase() {
        int i = 0;
        int rn = BusesTable.getRowCount();
        DefaultTableModel table = (DefaultTableModel) BusesTable.getModel();
        while (i < rn) {
            table.removeRow(0);
            i++;
        }
        try {
            Statement st;
            st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM bus;");
            while (rs.next()) {
                String Row[] = new String[]{rs.getString("IdBus"), rs.getString("Model"),
                    rs.getString("Enrollment"), rs.getString("PurchaseDate"), rs.getString("Driver"), rs.getString("PropertyOf")};
                table.addRow(Row);
            }
        } catch (SQLException ex) {
        }

    }
